#include "wolf.h"

void	put_gun(t_env *env)
{
	int x = 0;
	int y = 0;
	int pos = 0;

	while (y < 128)
	{
		x = 0;
		while (x < 128)
		{
			if (env->gun->data[128 * 128 - pos] != 0xFFFFFF)
				env->mlx.data[800 * 600 - (y * 800 + x) - 334] = env->gun->data[128 * 128 - pos]; 
			x++;
			pos++;
		}
		y++;
	}
	y = 0;
	while (y < 20)
	{
		x = 0;
		while (x < 20)
		{
			if ((x == 10 || y == 10) && x != y)
				env->mlx.data[800 * 300 + (y * 800 + x) + 390] = 0xFF00;
			x++;
		}
		y++;
	}
}
